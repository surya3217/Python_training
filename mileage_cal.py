"""
Name: Gas Mileage Calculator         
Problem Statement: Assume my car travels 100 Kilometres after putting 5 litres of fuel. 
                   Calculate the average of my car.
Hint: Divide kilmeters by the litres used to get the average
"""

fuel= float(input('Enter fuel in litres: '))
dist= float(input('Enter distance travelled in km: '))
print('Average of my car: ',dist/fuel,'km/lit')

